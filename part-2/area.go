package main

import "fmt"

func main() {

	// state question
	fmt.Println("What is the area of a circle with a radius of 14 cm?")

	// formula
	fmt.Println("A = pi * r * r")

	// put in known variables
	fmt.Println("A = 3.14 * 14 * 14")

	// solve the problem
	fmt.Println("A = " + fmt.Sprint(3.14*14) + " * 14")

	fmt.Println("A = " + fmt.Sprint(43.96*14))

	// final statement
	fmt.Println("The area of the circle is ~615.44cm")
	
	fmt.Println("\nDone.")

}
